import UIKit

extension UIColor {
        
    static func errorValidColor() -> UIColor {
        return #colorLiteral(red: 0.5783785502, green: 0.01568627544, blue: 0.1294117719, alpha: 1)
    }
    
    static func buttonRed() -> UIColor {
        return #colorLiteral(red: 0.8156862745, green: 0.007843137255, blue: 0.1058823529, alpha: 1)
    }
    
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.968627451, green: 0.9725490196, blue: 0.9921568627, alpha: 1)
    }
    
    static func buttonGreen() -> UIColor {
        return #colorLiteral(red: 0.1827126741, green: 0.6269522309, blue: 0.263020128, alpha: 1)
    }
    
    static func textFieldLight() -> UIColor {
        return #colorLiteral(red: 0.9019607843, green: 0.9019607843, blue: 0.9019607843, alpha: 1)
    }
    
    static func tabbarMainGray() -> UIColor {
        return #colorLiteral(red: 0.2235294118, green: 0.231372549, blue: 0.2549019608, alpha: 0.897687293)
    }
    
    static func tabbarUnselectedWhite() -> UIColor {
        return #colorLiteral(red: 0.968627451, green: 0.9725490196, blue: 0.9921568627, alpha: 0.5)
    }
}
